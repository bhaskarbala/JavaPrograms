package collections;

public class Bus {
	int busID;
	String busName;
	String busNo;
	

	/**
	 * it should be provide the Bus Details
	 * @param busID
	 * @param busName
	 * @param busNo
	 */
	public Bus(int busID, String busName, String busNo) {
		super();
		this.busID = busID;
		this.busName = busName;
		this.busNo = busNo;
	}
	

}
