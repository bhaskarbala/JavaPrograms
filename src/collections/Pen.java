package collections;

public class Pen {
	int penNo;
	String penCompany;
	String penType;
	String penColor;
	/**
	 * its should be provide the details of pen
	 * @param penNo
	 * @param penCompany
	 * @param penType
	 * @param penColor
	 */
	public Pen(int penNo, String penCompany, String penType, String penColor) {
		super();
		this.penNo = penNo;
		this.penCompany = penCompany;
		this.penType = penType;
		this.penColor = penColor;
	}

}
