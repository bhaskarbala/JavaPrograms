package colonableAndSeralizationInterface;

public class StudentSer {
	int sID;
	String Sname;
	char Sgarde;
	double smark;
	/**
	 * @param sID
	 * @param sname
	 * @param sgarde
	 * @param smark
	 */
	public StudentSer(int sID, String sname, char sgarde, double smark) {
		super();
		this.sID = sID;
		Sname = sname;
		Sgarde = sgarde;
		this.smark = smark;
	}

}
